package tw.org.iii.tutor;

public class ii18 {

	public static void main(String[] args) {
		int a =10;
		do {
			System.out.println(a);
		}while(a>10);
		System.out.println("---");
		while(a>3) {
			System.out.println(a--);
		}
		System.out.println("---");
		a=10;
		for(;a>3;) {
			System.out.println(a--);
		}
	}

}
